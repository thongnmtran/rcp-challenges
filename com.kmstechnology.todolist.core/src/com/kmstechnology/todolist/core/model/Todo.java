package com.kmstechnology.todolist.core.model;

public class Todo {
	private String todoString;

	public Todo() {
		super();
	}

	public Todo(String todoString) {
		super();
		this.todoString = todoString;
	}

	public String getText() {
		return todoString;
	}

	public void setTodo(String todoString) {
		this.todoString = todoString;
	}
}
